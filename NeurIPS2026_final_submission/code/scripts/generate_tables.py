# tab_emergence: matches unified_paper.tex L277-333 schema exactly
# 5 columns: Method | Params/agent | λ̄ | Survival % | Welfare
# ================================================================
def generate_tab_emergence():
    sources = [
        "ppo_nash_trap/ippo_results.json",
        "cleanrl_baselines/cleanrl_baseline_results.json",
        "cleanrl_baselines/iql_baseline_results.json",
        "cleanrl_baselines/qmix_baseline_results.json",
        "cleanrl_baselines/lola_results.json",
    ]

    ppo = load_json("ppo_nash_trap/ippo_results.json")
    cleanrl = load_json("cleanrl_baselines/cleanrl_baseline_results.json")
    iql_data = load_json("cleanrl_baselines/iql_baseline_results.json")
    qmix_data = load_json("cleanrl_baselines/qmix_baseline_results.json")
    lola_data = load_json("cleanrl_baselines/lola_results.json")

    lines = []
    lines.append("\\begin{tabular}{lcccc}")
    lines.append("\\toprule")
    lines.append("\\textbf{Method} & \\textbf{Params/agent} & $\\boldsymbol{\\bar{\\lambda}}$ & \\textbf{Survival \\%} & \\textbf{Welfare} \\\\")
    lines.append("\\midrule")

    # REINFORCE variants
    if ppo:
        algos = ppo.get("algorithms", {})
        for key, label, params in [
            ("ippo_linear", "Ind.\\ REINFORCE Linear", "5"),
            ("ippo_mlp", "Ind.\\ REINFORCE MLP", "161"),
            ("ippo_mlp_critic", "Ind.\\ REINFORCE MLP + Critic", "322"),
        ]:
            a = algos.get(key, {})
            lines.append(f"{label} & {params} & {fmt(a.get('lambda_mean',0), 3)} $\\pm$ {fmt(a.get('lambda_std',0), 3)} & {fmt(a.get('survival_mean',0))} & {fmt(a.get('welfare_mean',0))} \\\\")

    lines.append("\\midrule")
    lines.append("\\multicolumn{5}{c}{\\textit{PPO / Value Decomposition / Opponent-Shaping (20 seeds)}} \\\\")
    lines.append("\\midrule")

    # IPPO
    if cleanrl:
        ippo = cleanrl.get("CleanRL IPPO", {})
        lam = ippo.get("lambda", {})
        surv = ippo.get("survival", {})
        welf = ippo.get("welfare", {})
        lines.append(f"IPPO~\\citep{{schulman2017proximal}} & 4,546 & {fmt(lam.get('mean',0), 3)} $\\pm$ {fmt(lam.get('std',0), 3)} & {fmt(surv.get('mean',0))} & {fmt(welf.get('mean',0))} \\\\")

