#!/usr/bin/env python3
"""
generate_tables.py — Single Source of Truth: JSON → LaTeX tables.

Reads experiment outputs from code/outputs/*.json and generates
LaTeX table *body* files (tabular content only) in paper/tables/.
The paper includes these via \\input{tables/tab_*.tex} inside
\\begin{table} environments that own the caption/label/footnotes.

This structural separation makes manual copy-paste IMPOSSIBLE:
- Numbers live ONLY in JSON
- Table bodies are ONLY machine-generated
- Captions/footnotes stay in unified_paper.tex (human-authored)
- CI enforces: `python generate_tables.py --check`

Usage:
    python generate_tables.py          # Generate all table bodies
    python generate_tables.py --check  # CI: compare generated vs committed
"""
import json
import os
import sys
import hashlib
import datetime

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
OUTPUTS = os.path.join(SCRIPT_DIR, "..", "outputs")
TABLES_DIR = os.path.join(SCRIPT_DIR, "..", "..", "paper", "tables")


def load_json(rel_path):
    p = os.path.join(OUTPUTS, rel_path)
    if not os.path.exists(p):
        print(f"  WARNING: Missing {rel_path}")
        return None
    with open(p) as f:
        return json.load(f)


def fmt(val, decimals=1):
    if isinstance(val, str):
        return val
    return f"{val:.{decimals}f}"


def provenance_comment(json_files):
    ts = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    lines = [f"% AUTO-GENERATED by generate_tables.py at {ts}",
             f"% DO NOT EDIT MANUALLY — re-run generate_tables.py instead.",
             f"% Source JSON files:"]
    for jf in json_files:
        p = os.path.join(OUTPUTS, jf)
        if os.path.exists(p):
            h = hashlib.md5(open(p, 'rb').read()).hexdigest()[:8]
            lines.append(f"%   {jf} (md5:{h})")
    return "\n".join(lines)


